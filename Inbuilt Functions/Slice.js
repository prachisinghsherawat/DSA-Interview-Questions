

// SLICE() METHOD ----->


// The slice() method returns selected elements in an array, as a new array.
// The slice() method selects from a given start, up to a (not inclusive) given end.
// The slice() method does not change the original array.



let arr = [1,2,3,4,5]

let a = arr.slice(0,4)

console.log(a)



// OUTPUT -----> [ 1 , 2, 6 , 7 ]


